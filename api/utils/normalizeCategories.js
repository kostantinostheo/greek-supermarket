  export function normalizeCategory(category) {
    const normalized = category.toLowerCase();
  
    for (const [parent, keywords] of Object.entries(CATEGORY_MAPPINGS)) {
      if (keywords.some(keyword => normalized.includes(keyword))) {
        return parent;
      }
    }
  
    return category;
  }

  